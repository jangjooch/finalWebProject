package web.dto.item;

public class ItemDto {
	
	private int i_code;
	private String i_name;
	private int i_mount;
	private String i_class;
	private int i_weight;
	
	
	public int getI_code() {
		return i_code;
	}
	public void setI_code(int i_code) {
		this.i_code = i_code;
	}
	public String getI_name() {
		return i_name;
	}
	public void setI_name(String i_name) {
		this.i_name = i_name;
	}
	public int getI_mount() {
		return i_mount;
	}
	public void setI_mount(int i_mount) {
		this.i_mount = i_mount;
	}
	public String getI_class() {
		return i_class;
	}
	public void setI_class(String i_class) {
		this.i_class = i_class;
	}
	public int getI_weight() {
		return i_weight;
	}
	public void setI_weight(int i_weight) {
		this.i_weight = i_weight;
	}
	
	
	
}
